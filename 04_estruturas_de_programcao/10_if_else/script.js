let x = 10;
let y = 20;

if (x + y == 40) {
  console.log(`A soma de ${x} + ${y} é igual a 30.`);
} else if (x * y == 210) {
  console.log(`A multiplicação de ${x} + ${y} é igual a 200.`);
} else if (y / x == 3) {
  console.log(`A divisão de ${x} + ${y} é igual a 2.`);
} else {
  console.log(`Nenhum dos resultados foram alcançados!`);
}
